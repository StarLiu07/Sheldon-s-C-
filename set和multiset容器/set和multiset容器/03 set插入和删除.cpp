#include <iostream>
using namespace std;
#include <set>

/*
* 函数原型：
* insert(elem);  在容器中插入元素
* clear();  清除所有元素
* erase(pos);  删除pos迭代器所指的元素，返回下一个元素的迭代器
* erase(beg,end);  删除区间[beg,end]的所有元素，返回下一个元素的迭代器
* erase(elem);  删除容器中值为elem的元素
*/

void printset2(set<int>& s)
{
	for (set<int>::iterator it = s.begin(); it != s.end(); it++)
	{
		cout << *it << "  ";
	}
	cout << endl;
}

void test04()
{
	set<int> s1;

	//插入
	s1.insert(10);
	s1.insert(30);
	s1.insert(20);
	s1.insert(40);

	//遍历
	printset2(s1);

	//删除
	s1.erase(s1.begin());
	printset2(s1);

	//删除的重载版本
	s1.erase(30);
	printset2(s1);

	//清空
	//s1.erase(s1.begin(), s1.end());
	s1.clear();
	printset2(s1);

}

