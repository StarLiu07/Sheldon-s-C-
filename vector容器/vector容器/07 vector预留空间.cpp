#include <iostream>
using namespace std;
#include <vector>

//功能描述：减少vector在动态扩展容量时的扩展次数

//函数原型：reserve(int len);  //容器预留len个元素长度，预留位置不初始化，元素不可访问

void test08()
{
	vector<int> v1;

	//利用reserve预留空间
	v1.reserve(100000);

	int num = 0; //统计开辟的次数
	int* p = NULL;
	for (int i = 0; i < 100000; i++)
	{
		v1.push_back(i);

		if (p != &v1[0])
		{
			p = &v1[0];
			num++;
		}
	}

	cout << "num = " << num << endl;

}

int main() {

	test08();

	system("pause");

	return 0;
}